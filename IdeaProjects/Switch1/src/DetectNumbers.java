//A program to detect numbers by press them
public class DetectNumbers
{
    public static void main(String[] args)//main method
    {
        int num = 0;//creating variable
       //user should type number to get result
        switch (num)//creating switch statement
        {
            case 1:
                System.out.println("1");//printing out statement
                break;
            case 2:
                System.out.println("2");
                break;
            case 3:
                System.out.println("3");
                break;
            case 4:
                System.out.println("4");
                break;
            case 5:
                System.out.println("5");
                break;
            case 6:
                System.out.println("6");
                break;
            case 7:
                System.out.println("7");
                break;
            case 8:
                System.out.println("8");
                break;
            case 9:
                System.out.println("9");
                break;
            case 0:
                System.out.println("0");
                break;
            default:
                System.out.println("Not allowed");
        }

    }
}